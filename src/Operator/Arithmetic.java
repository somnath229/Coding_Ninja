package Operator;

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println(5/9); //int / int = int
        System.out.println(5.0/9); //deicmal / int =decimal

        System.out.println('a' + 3); // char + int = int            i.e. work on ascii values

        System.out.println(5/0); // error ArithmeticException

    }
}
